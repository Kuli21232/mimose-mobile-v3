export const supportedLanguages = ['ru', 'en'];

function normalizeLanguage(language) {
  return supportedLanguages.includes(language) ? language : 'ru';
}

export function useI18n() {
  const language = normalizeLanguage(useSettingsStore((state) => state.settings.language));
  const strings = translations[language];

  return {
    language,
    strings,
  };
}

export function getStrings(language) {
  return translations[normalizeLanguage(language)];
}
